 				Indonesias flora and fauna is divided by the Wallace Line that runs between Bali and Lombok, continuing north between Kalimantan and Sulawesi. West of the Line, vegetation and wildlife are Asian in nature, whereas east of the Line, these resemble those of Australia. Vegetation found in different parts of the archipelago varies according to rainfall, soil and altitude. On the wetter islands, on Sumatra, Kalimantan, and Papua, ancient rainforests cover large areas. These forests are rich in valuable hardwood, aromatic and spice trees, as well as exotic fruit trees. Lately, however, through illegal logging and human settlements, large tracts of forests have been decimated leaving infertile land that cause flooding and erosions. On the islands east of Bali known as the Nusatenggara islands (or once known as the Small Sunda Islands), there are savannahs, while on other mountain tops such as in the Mt. Gede National Park only 100 kms from Jakarta, one finds edelweiss, more reminiscent of Switzerland.
Discover-informationIndonesias wildlife varies from the Java mouse deer (or kancil) and the one-horned rhino to the Sumatran and Kalimantan Orang Utan, the Sulwesi anoa (a small water buffalo), the prehistoric giantKomodo lizard to the exotic Bird of Paradise in Papua.
How about flora? Here in Indonesia, you can find Raflesia Arnoldi in Bengkulu, one of the giant and unique flower in the world.
To preserve these unique flora and fauna Indonesia has designated 44 national parks throughout the archipelago, covering both land and sea, a large number of protected reserves offering ecotourism opportunities, as well as botanic gardens and zoos.
