				<h1 class="bold text-center">ULUWATU, HOME OF THE MOST FAMOUS WAVES IN BALI</h1>
				<hr>
				Uluwatu is home of the most famous waves in Bali. The spot offers several waves which are working with different swells and tides. The Peak is best at mid and high tide, closes out at low tide. In front of the cave, it's the most consistant. The Racetracks is at 100 meters further with fast wave, a lot of sections with easy tubes. Best at low tide and at 6 ft which can hold bigger swell. On the right tide and the right swell, 'The Peak' connects with 'Racetraks'. Outside corner is the real Uluwatu. Works only with big swells (>8 feet) and at low tide. The lower the tide, the better is the wave. Take at least a 7" board. It is a succession of long walls good for carving and, sometimes, a beautiful final tube.Temple is less surfed. Two waves in fact ("Outside temple" and "The Bombies"). Only for experts because the water is very shallow. For them, it is a incredible tube when it works. Various restaurants and cafes throughout Uluwatu, from cheap food stalls to luxury restaurants. Naturally, the souvenirs around here are sea-related products, such as items made of seashells and corals. But of course, there are also the modern kind of merchandises, such as T-shirts. There are many accommodations from warung (a traditional cafe), motels, hotels to villas. If you stay in a warung you just need to pay for the meals, while better accomodations like starred hotels are located near resorts.
				<h3 class="bold">Get There</h3>
				Uluwatu is about one hour drive from the Ngurah Rai International Airport. You can use a taxi from the airport. Or you can go to Denpasar and to find a travel agent or auto rental. Many rentals also provide motorcycles.
